public class Defense {
    private String armorabilBonus;
    private String classBonus;
    private String featBonus;
    private String enhancementBonus;
    private String miscBonus;

    public Defense() {
    }

    public String getArmorabilBonus() {
        return armorabilBonus;
    }

    public void setArmorabilBonus(String armorabilBonus) {
        this.armorabilBonus = armorabilBonus;
    }

    public String getClassBonus() {
        return classBonus;
    }

    public void setClassBonus(String classBonus) {
        this.classBonus = classBonus;
    }

    public String getFeatBonus() {
        return featBonus;
    }

    public void setFeatBonus(String featBonus) {
        this.featBonus = featBonus;
    }

    public String getEnhancementBonus() {
        return enhancementBonus;
    }

    public void setEnhancementBonus(String enhancementBonus) {
        this.enhancementBonus = enhancementBonus;
    }

    public String getMiscBonus() {
        return miscBonus;
    }

    public void setMiscBonus(String miscBonus) {
        this.miscBonus = miscBonus;
    }
}
